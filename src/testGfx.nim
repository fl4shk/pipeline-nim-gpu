macro `mkGfx16x16BramProcs`*(
  bramName: untyped,
  funcName: untyped,
  init: untyped,
): untyped = 
  let bramIdent = ident($bramName)
  let funcIdent = ident($funcName)
  let readFuncIdent = ident($bramName & "_RAM_DP_RF_1")
  let rdAddrIdent = ident("rdAddr")
  let wrAddrIdent = ident("wrAddrIdent")
  let wrDataIdent = ident("wrDataIdent")
  let wrEnIdent = ident("wrEnIdent")
  let locRdAddrIdent = ident("locRdAddr")
  let locWrAddrIdent = ident("locWrAddr")
  let locWrDataIdent = ident("locWrData")

  result = quote do:
    proc `readFuncIdent`(
      `rdAddrIdent`: uint8,
      `wrAddrIdent`: uint8,
      `wrDataIdent`: uint16,
      `wrEnIdent`: uint8,
    ): uint16 {.cnomangle,cnodecl.} =
      discard
    proc `funcIdent`*(
      `rdAddrIdent`: uint8,
    ): uint16 =
      var `bramIdent` {.cstatic.}: array[256, uint16] = `init`
      #result = 3
      var `locRdAddrIdent` {.cstatic.}: uint8 = 0
      var `locWrAddrIdent` {.cstatic.}: uint8 = 0
      var `locWrDataIdent` {.cstatic.}: uint16 = 0
      #var `locWrEnId` {.cstatic.}: uint8 = 1
      `locRdAddrIdent` = `rdAddrIdent`
      when doPipelineC:
        result = `readFuncIdent`(
          `rdAddrIdent`=`locRdAddrIdent`,
          `wrAddrIdent`=`locWrAddrIdent`,
          `wrDataIdent`=`locWrDataIdent`,
          `wrEnIdent`=1,
        )
      else:
        result = `bramIdent`[`locRdAddrIdent`]

